def test_run_generation_writes_background_prompt_when_enabled() -> None:
    root = Path("test_runtime") / f"scene_pipeline_bg_{uuid4().hex}"
    input_dir = root / "input"
    output_dir = root / "output"
    input_dir.mkdir(parents=True, exist_ok=True)
    output_dir.mkdir(parents=True, exist_ok=True)

    image_path = input_dir / "frame.png"
    Image.new("RGB", (160, 240), (120, 90, 60)).save(image_path)

    settings = Settings()
    settings.output_dir = output_dir

    args = Namespace(
        image=image_path,
        stage_id="scene_stage",
        scene_model="gpt-4.1",
        generate_video=False,
        generate_styled_images=False,
        prompt_model="gpt-4.1",
        motion_model="gpt-4.1",
    )

    def fake_scene_analyzer(_image_path: Path, _model: str | None) -> SceneAnalysis:
        return SceneAnalysis(
            summary="Wedding ring exchange scene.",
            people_count=2,
            people=[PersonInFrame(label="Bride"), PersonInFrame(label="Groom")],
            background="Ceremony room with warm curtains and soft indoor light.",
            shot_type="Medium shot.",
            main_action="The groom places a ring on the bride's finger.",
            mood=["warm", "ceremonial"],
            relationships=["Bride and groom at the center of the ceremony."],
        )

    def fake_prompt_synthesizer(**kwargs: object) -> PromptBundle:
        return PromptBundle(
            video_prompt="video prompt",
            video_prompt_ru="video prompt ru",
            final_frame_prompt="final frame prompt",
        )

    def fake_background_prompt_synthesizer(**kwargs: object) -> BackgroundPromptBundle:
        assert kwargs["stage_id"] == "scene_stage"
        return BackgroundPromptBundle(
            background_prompt="Create a cinematic horizontal 16:9 background image for the wedding scene.",
            background_prompt_ru="Создай кинематографичное горизонтальное изображение 16:9 для свадебной сцены.",
            association_prompt="Create a realistic associative architectural wedding interior with warm curtains and ceremony depth.",
            association_prompt_ru="Создай реалистичное ассоциативное архитектурное изображение свадебного интерьера.",
        )

    _run_generation(
        args,
        GenerationConfig(
            video_count=1,
            camera_segments=1,
            motion_source=MotionSource.TABLE,
            generate_final_frames=False,
            generate_source_background=True,
        ),
        settings=settings,
        scene_analyzer=fake_scene_analyzer,
        prompt_synthesizer=fake_prompt_synthesizer,
        background_prompt_synthesizer=fake_background_prompt_synthesizer,
        generate_final_frames=False,
    )

    bg_prompt_text = (output_dir / "scene_stage_bg_prompt.txt").read_text(encoding="utf-8")
    bg_prompt_ru_text = (output_dir / "scene_stage_bg_prm_ru.txt").read_text(encoding="utf-8")
    assoc_bg_prompt_text = (output_dir / "scene_stage_assoc_bg_prompt.txt").read_text(encoding="utf-8")
    assoc_bg_prompt_ru_text = (output_dir / "scene_stage_assoc_bg_prm_ru.txt").read_text(encoding="utf-8")
    assert "horizontal 16:9 background image" in bg_prompt_text
    assert "горизонтальное изображение 16:9" in bg_prompt_ru_text
    assert "realistic associative architectural wedding interior" in assoc_bg_prompt_text
    assert "ассоциативное архитектурное изображение" in assoc_bg_prompt_ru_text
